// Función para obtener áreas por especialidad
export const getAreasByEspecialidad = (especialidad) => {
  return AREAS_INTERES[especialidad] || [];
};

// Función para obtener todas las especialidades disponibles
export const getEspecialidadesDisponibles = () => {
  return Object.keys(AREAS_INTERES);
};

// Función para buscar áreas que contengan un texto
export const searchAreas = (searchText) => {
  const results = [];
  Object.entries(AREAS_INTERES).forEach(([especialidad, areas]) => {
    const matchingAreas = areas.filter(area => 
      area.toLowerCase().includes(searchText.toLowerCase())
    );
    if (matchingAreas.length > 0) {
      results.push({
        especialidad,
        areas: matchingAreas
      });
    }
  });
  return results;
};

// Función para validar si un área pertenece a una especialidad
export const isValidArea = (especialidad, area) => {
  const areas = getAreasByEspecialidad(especialidad);
  return areas.includes(area);
};